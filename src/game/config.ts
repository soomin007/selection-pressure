// 런/라운드 구조 상수 (Phase 4~5). 한 라운드 길이·런당 라운드 수.
// (참고: Everything is Crab ≈ 20분/보스3 → 모바일 호흡에 맞게 축소)

export const GAME = {
  roundSeconds: 16, // 채집 라운드 길이(초) — 통과 기준 없어 짧혀도 밸런스 영향 없음
  bossSeconds: 20, // 보스 게이트 관전 길이(초) — 통과 기준이 이 길이에 맞춰져 있음
  extinctionSeconds: 24, // 대멸종 피날레 길이(초) — 통과 기준이 이 길이에 맞춰져 있음
  bossPassThreshold: 3, // 보스 끝까지 내 종이 이 수 이상 생존하면 통과 (소수 개체 게임에 맞춰 낮춤)
  extinctionPassThreshold: 3, // 대멸종 끝까지 내 종이 이 수 이상 생존하면 통과(승리, 클라이맥스 필터)

  // --- 레벨업(형질 성장) — 시간이 아니라 경험치로 형질을 얻는다(먹이 섭취 = 경험치). ---
  // 필요 경험치 = xpBase + (레벨-1) × xpPerLevel. 경험치 = **내 종이 먹은 먹이 개수**.
  // ⚠ 개체 수로 나누지 않으므로 무리가 커질수록 레벨업이 빨라진다(18마리면 16초 라운드에 30~40개).
  // 24/14 는 너무 빨랐다 — 한 런에 드래프트가 **7번** 떴다(사용자: "레벨업 드래프트가 엄청 빠르게 자주 뜬다").
  // 40/22 로 올리면 5.4번으로 줄면서 도달 단계는 그대로다(5.5 → 5.6). 더 올리면(55/30) 카드가 모자라
  // 오히려 약해진다(도달 4.3) — 성장이 위협(시대마다 +22%)을 못 따라가기 때문. 40/22 가 균형점.
  xpBase: 40, // 레벨 1→2 에 필요한 경험치(내 종이 먹은 먹이 수)
  xpPerLevel: 22, // 레벨이 오를수록 필요 경험치가 이만큼씩 늘어난다(뒤로 갈수록 느긋)

  // --- 위협 예고 — 보스/대멸종 단계 시작 이 초 전에 전광판으로 미리 알린다(마음의 준비). ---
  threatPreviewLead: 4,

  // --- 다시 뽑기(리롤) — 여러 런을 마치면 열리는 편의(meta.ts). 한 드래프트에서 이만큼 새로 뽑을 수 있다. ---
  rerollsPerDraft: 1, // 드래프트당 리롤 횟수(운 다시 굴리기. 무한 낚시 방지로 소수)

  // --- 난이도 루프(승리 후 진행) — 승리하면 "다음 시대"로 이어가며 위협이 점점 세진다(brotato식). ---
  // era 0 = 첫 시대(배율 1.0 = 기존과 완전 동일 → 통과기준 테스트 보존). 승리마다 era +1.
  // 통과기준(생존 수)은 그대로 두고, 위협 강도(보스·대멸종)만 이 계단으로 키운다(소수 개체라 안전).
  eraDifficultyStep: 0.22, // 시대마다 위협 강도 배율 +22% (era 1 → ×1.22, era 2 → ×1.44 …)
  // 세계 척박화 — 시대가 지날수록 먹이가 줄고 재생이 느려진다(회복 억제). 위협 강도(eraDifficultyStep)만으론
  // "3마리만 남으면 통과"라 체감이 약했다(진단: 보스가 개체수를 거의 안 깎음). 채집 회복을 억제해 무리가
  // 얇아진 채 위협을 맞게 하면, 위협이 실제로 무리를 위협한다. era 0 = ×1.0(기존과 동일 → 통과기준 보존).
  eraScarcityStep: 0.14, // 시대마다 먹이 척박 배율 +14% (era 1 → 먹이 ÷1.14·재생 ×1.14 …)
  eraCap: 5, // 시대 상한 — 이 시대(=시대 5)의 대멸종까지 넘으면 "정복"(최종 승리). 그 전까지는 "다음 시대로".

  // --- 시대 보상 — 시대를 넘을 때마다 강화된 카드 하나를 고른다(난이도 루프를 "갈수록 재밌게"). ---
  // 위협이 시대마다 +22% 세지므로, 정상 카드의 이 배수로 키운 보상으로 큰 도약을 줘 성장이 위협을 앞서게 한다.
  eraRewardBoost: 2, // 보상 카드 효과 = 정상 카드 ×2 (표시값·실제값 함께 커진다 — boostCard 로 사본 생성)
} as const;

/**
 * 시대(era)별 위협 강도 배율. era 0 = 1.0(기존과 동일 → 통과기준 테스트 보존).
 *
 * **복리(지수)다.** 예전엔 선형(1 + era×0.22)이라 시대를 거듭할수록 난이도 상승이 성장에 뒤처졌다
 * (사용자: "난이도는 선형이 아니라 지수적으로 상승해야 해"). 카드 성장은 곱셈처럼 쌓이는데 위협만
 * 덧셈으로 오르면 후반이 시시해진다.
 *   선형: 1.22 · 1.44 · 1.66 · 1.88 · 2.10
 *   복리: 1.22 · 1.49 · 1.82 · 2.22 · 2.72  ← 지금
 */
export function eraDifficulty(era: number): number {
  return Math.pow(1 + GAME.eraDifficultyStep, Math.max(0, era));
}

/**
 * 시대(era)별 먹이 척박 배율 — 클수록 먹이가 적고 재생이 느리다(위협 사이 회복 억제). era 0 = 1.0.
 * 위협 강도(eraDifficulty)와 짝을 이룬다: 위협이 세지는 동시에 세계가 척박해져, 무리가 얇아진 채 위협을
 * 맞으므로 "오래오래 살다 대멸종에서 갈린다"가 아니라 시대마다 실제로 조여든다.
 *   복리: 1.14 · 1.30 · 1.48 · 1.69 · 1.93 (era 1~5)
 */
export function eraScarcity(era: number): number {
  return Math.pow(1 + GAME.eraScarcityStep, Math.max(0, era));
}

